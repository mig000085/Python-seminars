# Посчитайте сумму чётных элементов от 1 до n исключая кратные e.
# Используйте while и if.
# Попробуйте разные значения e и n 

n, e = 10, 3
new_sum = 0
elem = 0
while elem <= n:
    elem +=1
    if elem % 2 == 0:
        if elem % e == 0:
            continue
        new_sum += elem
print(f'Сумма: {new_sum}')

#Задаем значения n и e (можно менять для проверки)
n = 10
e = 3

total = 0  # Инициализация суммы
i = 1      # Счетчик

while i <= n:
    # Проверка на четность и отсутствие кратности e
    if i % 2 == 0 and i % e != 0:
        total += i
    i += 1  # Увеличиваем счетчик

print(f'Сумма: {total}')